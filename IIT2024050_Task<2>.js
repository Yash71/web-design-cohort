const Fullname=document.querySelector(".Name");
const FullAge=document.querySelector(".Age");

document.querySelector(".press").addEventListener("click", ()=>{
    document.getElementById("name").innerHTML+=Fullname.value;
    document.getElementById("age").innerHTML+=FullAge.value;
    Fullname.value="";
    FullAge.value="";
});


let drop=document.querySelector(".font_style");
document.querySelector(".change").addEventListener("click", ()=>{
    let fontstyle=drop.value;
    document.querySelector("body").style.fontFamily=fontstyle;
});
